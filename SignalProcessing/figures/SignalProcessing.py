from scipy import signal
# Параметри фільтру та сигналу
order = 3  # порядок фільтру
F_max = 5  # максимальна частота сигналу в Гц
Fs = 1000  # частота дискретизації в Гц

# Нормування частоти
w = F_max / (Fs / 2)

# Розрахунок параметрів фільтру низьких частот
b, a = signal.butter(order, w, 'low', output='ba')

# Результат
print("Коефіцієнти чисельника (b):", b)
print("Коефіцієнти знаменника (a):", a)
